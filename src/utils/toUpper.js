/**
 * All down to uppercase
 *
 * @since 1.0.0;
 * @category string;
 * @author fl;
 * @param str {string} the first string incoming;
 * @return {string};
 * @create_date 2018/07/06;
 * @modify_date 2018/07/06;
 * @example
 *
 * toUpper('aaa')
 * // => 'AAA'
 */

function toUpper(str) {
  return str.toUpperCase();
}

export default toUpper;
